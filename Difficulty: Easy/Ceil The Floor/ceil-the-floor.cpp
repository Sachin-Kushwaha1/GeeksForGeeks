class Solution {
  public:
    vector<int> getFloorAndCeil(int x, vector<int> &arr) {
        // code here
        int fl = 0, cl = INT_MAX;
        for(auto it:arr){
            if(it<=x && it>fl) fl = it;
            if( it>=x && it<cl ) cl = it;
        }
        if(fl == 0) fl=-1;
        if(cl == INT_MAX) cl=-1;
        return {fl,cl};
    }
};

